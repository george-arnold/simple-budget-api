CREATE TABLE login (
id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
hash VARCHAR(100),
email TEXT NOT NULL
);

CREATE TABLE users (
id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
name VARCHAR(50),
email TEXT NOT NULL,
joined TIMESTAMP NOT NULL
);

ALTER TABLE categories 
ADD COLUMN 
user_id INTEGER REFERENCES users(id)
ON DELETE SET NULL;

ALTER TABLE transactions
ADD COLUMN 
user_id INTEGER REFERENCES users(id)
ON DELETE SET NULL;